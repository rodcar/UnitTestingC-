using System;

namespace OperacionesMatematicas
{
    public class OperacionesMatematicas
    {

        public int suma(int a, int b)
        {
            return a + b;
        }

        public int resta(int a, int b)
        {
                return a - b;
        }

        public int multiplicar(int a, int b)
        {
            return a * b;
        }

        public float division(float a, float b)
        {
            return a / b;
        }
    }
}
